int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	while ((*s1 != '\0' && *s2 != '\0') && *s1 == *s2 && n > 0)
	{
		s1++;
		s2++;
	}
	if (*s1 == *s2)
		return (0);
	else
		return (*s1 - *s2);
}